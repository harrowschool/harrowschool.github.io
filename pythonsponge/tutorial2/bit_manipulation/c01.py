# 5 is stored as 0b101
# 6 is stored as 0b110
# Both have 1's: 0b100 = 4
print("5 AND 6 equals", 5 & 6)

# 5 is stored as 0b101
# 6 is stored as 0b110
# Either has 1:  0b111 = 7
print("5 OR 6 equals", 5 | 6)

# 5 is stored as 0b101
# 6 is stored as 0b110
# Inputs differ: 0b011 = 3
print("5 XOR 6 equals", 5 ^ 6)
